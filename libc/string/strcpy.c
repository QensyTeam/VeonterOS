#include <stddef.h>

// Функция для копирования содержимого одной строки в другую
char* strcpy(char* dest, const char* src) {
 // Проверить корректность аргументов
 if (dest == NULL || src == NULL) {
 return NULL;
 }

 // Объявить переменную для хранения индекса в строках
 size_t i = 0;

 // Цикл по символам исходной строки
 while (src[i] != '\0') {
 // Копировать символ в целевую строку
 dest[i] = src[i];

 // Перейти к следующему символу
 i++;
 }

 // Добавить нулевой символ в конец целевой строки
 dest[i] = '\0';

 // Вернуть указатель на целевую строку
 return dest;
}
